import React, { createContext, useContext, useEffect, useState, ReactNode } from 'react';
import { User, Session } from '@supabase/supabase-js';
import { supabase } from '@/integrations/supabase/client';
import { useToast } from '@/hooks/use-toast';

interface AuthContextType {
  user: User | null;
  session: Session | null;
  loading: boolean;
  isAdmin: boolean;
  userRole: string | null;
  subscriptionStatus: {
    subscribed: boolean;
    subscription_tier: string | null;
    subscription_end: string | null;
  };
  checkSubscription: () => Promise<void>;
  signIn: (email: string, password: string) => Promise<{ error: any }>;
  signUp: (email: string, password: string, fullName?: string) => Promise<{ error: any }>;
  signInWithGoogle: () => Promise<{ error: any }>;
  signOut: () => Promise<void>;
  resetPassword: (email: string) => Promise<{ error: any }>;
  refreshUserRole: () => Promise<void>;
}

const AuthContext = createContext<AuthContextType | undefined>(undefined);

export const AuthProvider: React.FC<{ children: ReactNode }> = ({ children }) => {
  const [user, setUser] = useState<User | null>(null);
  const [session, setSession] = useState<Session | null>(null);
  const [loading, setLoading] = useState(true);
  const [isAdmin, setIsAdmin] = useState(false);
  const [userRole, setUserRole] = useState<string | null>(null);
  const [subscriptionStatus, setSubscriptionStatus] = useState({
    subscribed: false,
    subscription_tier: null,
    subscription_end: null,
  });
  const [adminStateInitialized, setAdminStateInitialized] = useState(false);
  const { toast } = useToast();

  // ESTADO ADMIN INMUTABLE - Configuraci√≥n enterprise permanente
  const ADMIN_ENTERPRISE_STATE = {
    subscribed: true,
    subscription_tier: 'enterprise',
    subscription_end: null,
  };

  // Funciones de persistencia mejoradas
  const getAdminStateKey = (userId: string) => `admin_state_v3_${userId}`;
  
  const setAdminStatePermanently = (userId: string) => {
    console.log('üîß [ADMIN] ESTABLECIENDO ESTADO ADMIN PERMANENTE para:', userId);
    
    // Estados s√≠ncronos INMUTABLES
    setIsAdmin(true);
    setUserRole('admin');
    setSubscriptionStatus({ ...ADMIN_ENTERPRISE_STATE });
    setAdminStateInitialized(true);
    
    // Persistencia robusta
    const adminData = {
      isAdmin: true,
      userRole: 'admin',
      subscriptionStatus: { ...ADMIN_ENTERPRISE_STATE },
      timestamp: Date.now(),
      version: 3
    };
    
    localStorage.setItem(getAdminStateKey(userId), JSON.stringify(adminData));
    console.log('‚úÖ [ADMIN] Estado admin persistido permanentemente e inmutable');
  };

  const loadPersistedAdminState = (userId: string): boolean => {
    console.log('üíæ [ADMIN] Verificando estado admin persistido para:', userId);
    
    try {
      const stateKey = getAdminStateKey(userId);
      const persistedState = localStorage.getItem(stateKey);
      
      if (persistedState) {
        const state = JSON.parse(persistedState);
        
        // Verificar versi√≥n y validez
        if (state.version === 3 && state.isAdmin && state.userRole === 'admin') {
          console.log('üöÄ [ADMIN] RESTAURANDO estado admin desde localStorage');
          
          // Restauraci√≥n s√≠ncrona e inmutable
          setIsAdmin(true);
          setUserRole('admin');
          setSubscriptionStatus({ ...ADMIN_ENTERPRISE_STATE });
          setAdminStateInitialized(true);
          
          return true;
        } else {
          // Limpiar versiones antiguas
          localStorage.removeItem(stateKey);
        }
      }
    } catch (error) {
      console.error('‚ùå [ADMIN] Error cargando estado admin persistido:', error);
    }
    
    return false;
  };

  const clearAdminState = (userId?: string) => {
    console.log('üßπ [ADMIN] Limpiando estado admin');
    setIsAdmin(false);
    setUserRole('user');
    setAdminStateInitialized(false);
    
    if (userId) {
      localStorage.removeItem(getAdminStateKey(userId));
    }
  };

  // GUARDIA CR√çTICA - Protege contra verificaciones de suscripci√≥n para admins
  const isAdminProtected = () => {
    const adminStatus = isAdmin || userRole === 'admin';
    if (adminStatus) {
      console.log('üõ°Ô∏è [ADMIN] GUARDIA ACTIVADA - Operaci√≥n bloqueada para admin');
    }
    return adminStatus;
  };

  // Funci√≥n principal para verificar role - MEJORADA con prioridad admin
  const refreshUserRole = async () => {
    if (!session?.user) {
      clearAdminState();
      setSubscriptionStatus({
        subscribed: false,
        subscription_tier: null,
        subscription_end: null,
      });
      setAdminStateInitialized(true);
      return;
    }
    
    const userId = session.user.id;
    
    // PASO 1: Verificar estado persistido PRIMERO (S√çNCRONO)
    if (loadPersistedAdminState(userId)) {
      console.log('üöÄ [ADMIN] Admin state restaurado - SALTANDO verificaci√≥n DB');
      return;
    }
    
    try {
      console.log('üîç [AUTH] Verificando role en base de datos para:', session.user.email);
      
      const { data: roleData, error: roleError } = await supabase
        .rpc('get_user_role', { _user_id: userId });

      console.log('üëë [AUTH] Resultado verificaci√≥n role:', { roleData, roleError });
      
      if (!roleError && roleData === 'admin') {
        // USUARIO ES ADMIN - Estado inmutable y permanente
        setAdminStatePermanently(userId);
        console.log('üéØ [ADMIN] ADMIN CONFIRMADO - Estado establecido permanentemente');
      } else {
        // Usuario regular - limpiar cualquier estado admin residual
        clearAdminState(userId);
        setUserRole(roleData || 'user');
        setAdminStateInitialized(true);
        
        // SOLO para usuarios NO-admin verificar suscripci√≥n
        console.log('üë§ [AUTH] Usuario regular detectado - verificando suscripci√≥n...');
        await checkSubscriptionForRegularUser();
      }
    } catch (error) {
      console.error('‚ùå [AUTH] Error verificando role:', error);
      clearAdminState(userId);
      setUserRole('user');
      setAdminStateInitialized(true);
    }
  };

  // Funci√≥n separada para verificar suscripci√≥n SOLO para usuarios regulares
  const checkSubscriptionForRegularUser = async () => {
    if (!session?.user) return;
    
    // GUARDIA CR√çTICA - Nunca ejecutar para admins
    if (isAdminProtected()) {
      console.log('üö´ [ADMIN] checkSubscriptionForRegularUser bloqueado para admin');
      return;
    }
    
    try {
      console.log('üîç [SUB] Verificando suscripci√≥n para usuario regular');

      const { data: dbData, error: dbError } = await supabase
        .from('subscribers')
        .select('*')
        .eq('user_id', session.user.id)
        .single();

      console.log('üìä [SUB] Datos suscripci√≥n desde DB:', { dbData, dbError });

      if (!dbError && dbData) {
        const newStatus = {
          subscribed: dbData.subscribed || false,
          subscription_tier: dbData.subscription_tier || null,
          subscription_end: dbData.subscription_end || null,
        };
        console.log('‚úÖ [SUB] Estableciendo estado suscripci√≥n regular:', newStatus);
        setSubscriptionStatus(newStatus);
        return;
      }

      // Fallback a funci√≥n edge
      console.log('‚ö†Ô∏è [SUB] Sin datos en DB, probando funci√≥n edge...');
      const { data, error } = await supabase.functions.invoke('check-subscription');
      if (error) {
        console.error('‚ùå [SUB] Error verificando suscripci√≥n via funci√≥n:', error);
        return;
      }
      
      const fallbackStatus = {
        subscribed: data.subscribed || false,
        subscription_tier: data.subscription_tier || null,
        subscription_end: data.subscription_end || null,
      };
      console.log('‚úÖ [SUB] Estableciendo estado suscripci√≥n fallback:', fallbackStatus);
      setSubscriptionStatus(fallbackStatus);
    } catch (error) {
      console.error('‚ùå [SUB] Error verificando suscripci√≥n:', error);
    }
  };

  // Funci√≥n checkSubscription p√∫blica - CON GUARDIA CR√çTICA MEJORADA
  const checkSubscription = async () => {
    if (!session?.user) {
      console.log('‚ö†Ô∏è [SUB] No hay usuario, saltando checkSubscription');
      return;
    }
    
    // GUARDIA ABSOLUTA - Nunca verificar suscripci√≥n para admins
    if (isAdminProtected()) {
      console.log('üö´ [ADMIN] checkSubscription BLOQUEADO para admin');
      toast({
        title: "Usuario Admin",
        description: "Los administradores tienen acceso ilimitado autom√°ticamente.",
        variant: "default",
      });
      return;
    }
    
    // Verificar estado persistido por si acaso
    if (loadPersistedAdminState(session.user.id)) {
      console.log('üöÄ [ADMIN] Estado admin restaurado durante checkSubscription - SALTANDO');
      return;
    }
    
    // Esperar a que la inicializaci√≥n admin est√© completa
    if (!adminStateInitialized) {
      console.log('‚è≥ [AUTH] Esperando inicializaci√≥n admin...');
      setTimeout(() => checkSubscription(), 500);
      return;
    }
    
    await checkSubscriptionForRegularUser();
  };

  const signIn = async (email: string, password: string) => {
    try {
      const { error } = await supabase.auth.signInWithPassword({
        email,
        password,
      });
      
      if (error) {
        let errorMessage = 'Error al iniciar sesi√≥n';
        if (error.message.includes('Invalid login credentials')) {
          errorMessage = 'Credenciales inv√°lidas. Verifica tu email y contrase√±a.';
        } else if (error.message.includes('Email not confirmed')) {
          errorMessage = 'Por favor confirma tu email antes de iniciar sesi√≥n.';
        }
        
        toast({
          title: "Error de autenticaci√≥n",
          description: errorMessage,
          variant: "destructive",
        });
      } else {
        toast({
          title: "¬°Bienvenido de vuelta!",
          description: "Has iniciado sesi√≥n exitosamente.",
        });
      }
      
      return { error };
    } catch (error) {
      toast({
        title: "Error inesperado",
        description: "Ocurri√≥ un error durante el inicio de sesi√≥n.",
        variant: "destructive",
      });
      return { error };
    }
  };

  const signUp = async (email: string, password: string, fullName?: string) => {
    try {
      const { error } = await supabase.auth.signUp({
        email,
        password,
        options: {
          data: {
            full_name: fullName,
          },
          emailRedirectTo: `${window.location.origin}/`,
        },
      });
      
      if (error) {
        let errorMessage = 'Error al crear la cuenta';
        if (error.message.includes('User already registered')) {
          errorMessage = 'Este email ya est√° registrado. Intenta iniciar sesi√≥n.';
        } else if (error.message.includes('Password should be at least')) {
          errorMessage = 'La contrase√±a debe tener al menos 6 caracteres.';
        }
        
        toast({
          title: "Error de registro",
          description: errorMessage,
          variant: "destructive",
        });
      } else {
        toast({
          title: "¬°Cuenta creada!",
          description: "Revisa tu email para confirmar tu cuenta.",
        });
      }
      
      return { error };
    } catch (error) {
      toast({
        title: "Error inesperado",
        description: "Ocurri√≥ un error durante el registro.",
        variant: "destructive",
      });
      return { error };
    }
  };

  const signInWithGoogle = async () => {
    try {
      const { error } = await supabase.auth.signInWithOAuth({
        provider: 'google',
        options: {
          redirectTo: `${window.location.origin}/dashboard`,
        },
      });
      return { error };
    } catch (error) {
      toast({
        title: "Error de autenticaci√≥n",
        description: "No se pudo conectar con Google.",
        variant: "destructive",
      });
      return { error };
    }
  };

  const resetPassword = async (email: string) => {
    try {
      const { error } = await supabase.auth.resetPasswordForEmail(email, {
        redirectTo: `${window.location.origin}/auth?mode=reset`,
      });
      
      if (error) {
        toast({
          title: "Error",
          description: "No se pudo enviar el email de recuperaci√≥n.",
          variant: "destructive",
        });
      } else {
        toast({
          title: "Email enviado",
          description: "Revisa tu email para restablecer tu contrase√±a.",
        });
      }
      
      return { error };
    } catch (error) {
      toast({
        title: "Error inesperado",
        description: "Ocurri√≥ un error al solicitar la recuperaci√≥n.",
        variant: "destructive",
      });
      return { error };
    }
  };

  const signOut = async () => {
    try {
      console.log('üö™ [AUTH] Iniciando logout...');
      
      // Limpiar localStorage para este usuario
      if (user?.id) {
        localStorage.removeItem(getAdminStateKey(user.id));
      }
      
      // Limpiar estados locales ANTES de signOut
      setUser(null);
      setSession(null);
      setIsAdmin(false);
      setUserRole(null);
      setAdminStateInitialized(false);
      setSubscriptionStatus({
        subscribed: false,
        subscription_tier: null,
        subscription_end: null,
      });
      
      const { error } = await supabase.auth.signOut();
      
      if (error) {
        console.error('‚ùå [AUTH] Error durante signOut:', error);
        toast({
          title: "Error",
          description: "Ocurri√≥ un error al cerrar sesi√≥n.",
          variant: "destructive",
        });
      } else {
        console.log('‚úÖ [AUTH] Logout exitoso');
        toast({
          title: "Sesi√≥n cerrada",
          description: "Has cerrado sesi√≥n exitosamente.",
        });
        
        window.location.href = '/';
      }
    } catch (error) {
      console.error('‚ùå [AUTH] Error inesperado durante signOut:', error);
      toast({
        title: "Error",
        description: "Ocurri√≥ un error al cerrar sesi√≥n.",
        variant: "destructive",
      });
    }
  };

  // Effect principal - MEJORADO con inicializaci√≥n prioritaria de admin
  useEffect(() => {
    let mounted = true;

    const { data: { subscription } } = supabase.auth.onAuthStateChange(
      async (event, session) => {
        if (!mounted) return;
        
        console.log('üîê [AUTH] Cambio estado auth:', event, session?.user?.email);
        
        setSession(session);
        setUser(session?.user ?? null);
        setLoading(false);
        
        if (event === 'SIGNED_IN' && session) {
          console.log('‚úÖ [AUTH] Usuario autenticado, inicializando estado...');
          
          // PRIORIDAD M√ÅXIMA: Verificar estado admin persistido INMEDIATAMENTE
          if (loadPersistedAdminState(session.user.id)) {
            console.log('üöÄ [ADMIN] Estado admin restaurado inmediatamente - SIN verificaci√≥n DB');
            return;
          }
          
          // Solo si NO hay estado persistido, verificar en DB
          await refreshUserRole();
        }
        
        if (event === 'SIGNED_OUT') {
          console.log('üö™ [AUTH] Usuario desconectado, reseteando estados');
          clearAdminState();
          setSubscriptionStatus({
            subscribed: false,
            subscription_tier: null,
            subscription_end: null,
          });
          setAdminStateInitialized(true);
        }
      }
    );

    // Verificar sesi√≥n existente con prioridad admin
    supabase.auth.getSession().then(async ({ data: { session }, error }) => {
      if (!mounted) return;
      
      if (error) {
        console.error('‚ùå [AUTH] Error obteniendo sesi√≥n:', error);
      }
      console.log('üîê [AUTH] Verificaci√≥n sesi√≥n inicial:', session?.user?.email);
      setSession(session);
      setUser(session?.user ?? null);
      setLoading(false);
      
      if (session && mounted) {
        console.log('‚úÖ [AUTH] Sesi√≥n existente encontrada, inicializando estado...');
        
        // PRIORIDAD M√ÅXIMA: Verificar estado admin persistido PRIMERO
        if (loadPersistedAdminState(session.user.id)) {
          console.log('üöÄ [ADMIN] Estado admin restaurado desde persistencia en carga inicial');
          return;
        }
        
        // Solo si NO hay estado persistido, verificar en DB
        await refreshUserRole();
      } else {
        setAdminStateInitialized(true);
      }
    });

    return () => {
      mounted = false;
      subscription.unsubscribe();
    };
  }, []);

  const value = {
    user,
    session,
    loading,
    isAdmin,
    userRole,
    subscriptionStatus,
    checkSubscription,
    signIn,
    signUp,
    signInWithGoogle,
    signOut,
    resetPassword,
    refreshUserRole,
  };

  return (
    <AuthContext.Provider value={value}>
      {children}
    </AuthContext.Provider>
  );
};

export function useAuth() {
  const context = useContext(AuthContext);
  if (context === undefined) {
    throw new Error('useAuth must be used within an AuthProvider');
  }
  return context;
}
